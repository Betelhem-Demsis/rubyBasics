# Initialize x with a value of 1
x = 1

# While loop that continues as long as x is less than or equal to 5
while x <= 5
  # Output the current value of x
  puts x
  # Increment x by 1
  x += 1
end

# Alternative approach using an until loop
y = 1

# Until loop that runs until y is greater than 5
until y > 5
  # Output the current value of y
  puts y
  # Increment y by 1
  y += 1
end
